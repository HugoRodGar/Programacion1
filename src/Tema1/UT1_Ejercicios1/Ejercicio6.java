package Tema1.UT1_Ejercicios1;

public class Ejercicio6 {

    public static void main(String[] args) {

        int N;
        double A;
        char C;

        N = 1;
        A = 2.2;
        C = 'H';

        System.out.println("N tiene el valor: " + N);
        System.out.println("A tiene el valor: " + A);
        System.out.println("C tiene el valor: " + C);

        // N + A
        double ResultadoNA = N + A;
        System.out.println("El resultado de N + A es: " + ResultadoNA);

        // A - N
        double ResultadoAN = A - N;
        System.out.println("El resultado de A - N es: " + ResultadoAN);

        // Valor numerico de C
        System.out.println("El valor numerico de C es: " + C);

    }
}
