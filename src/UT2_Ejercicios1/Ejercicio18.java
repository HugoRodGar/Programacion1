package UT2_Ejercicios1;

public class Ejercicio18 {

    public static void main(String[] args) {

        long numero = 1;

        for (int i = 1; i <= 20; i++) {
            numero = numero * i;
        }

        System.out.println("El resultado de la multipliocacion de los primero 20 numeros naturales es: " + numero);

    }
}
